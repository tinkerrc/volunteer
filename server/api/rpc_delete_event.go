package api

import (
	"context"
	"errors"

	"connectrpc.com/connect"

	"github.com/google/uuid"
	apiv1 "github.com/tinkerrc/volunteer/proto/api/v1" // generated by protoc-gen-go
)

func (s *APIServer) DeleteEvent(
	ctx context.Context,
	req *connect.Request[apiv1.DeleteEventRequest],
) (*connect.Response[apiv1.DeleteEventResponse], error) {
	err := s.ensureAdmin(ctx)
	if err != nil {
		return nil, err
	}
	m := req.Msg
	eid, err := uuid.Parse(m.Id)
	if err != nil {
		return nil, connect.NewError(connect.CodeInvalidArgument, errors.New("invalid event id"))
	}
	err = s.Db.Event.DeleteOneID(eid).Exec(ctx)
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, errors.New("could not delete event"))
	}
	res := connect.NewResponse(&apiv1.DeleteEventResponse{})
	return res, nil
}
