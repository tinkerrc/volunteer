package api

import (
	"context"
	"errors"

	"connectrpc.com/connect"

	"github.com/google/uuid"
	apiv1 "github.com/tinkerrc/volunteer/proto/api/v1" // generated by protoc-gen-go
)

func (s *APIServer) DeleteCert(
	ctx context.Context,
	req *connect.Request[apiv1.DeleteCertRequest],
) (*connect.Response[apiv1.DeleteCertResponse], error) {
	err := s.ensureAdmin(ctx)
	if err != nil {
		return nil, err
	}
	cid, err := uuid.Parse(req.Msg.Id)
	if err != nil {
		return nil, connect.NewError(connect.CodeInvalidArgument, errors.New("invalid cert id"))
	}
	err = s.Db.Cert.DeleteOneID(cid).Exec(ctx)
	if err != nil {
		return nil, connect.NewError(connect.CodeFailedPrecondition, errors.New("cannot delete cert"))
	}
	res := connect.NewResponse(&apiv1.DeleteCertResponse{})
	return res, nil
}
